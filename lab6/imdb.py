import db
from rating import Rating


class ImDb(db.Database):
    def __init__(self):
        super().__init__('imdb.db')
        self.ratings = []
        self.__create_ratings__()

    def __create_ratings__(self):
        self.execute_sql('''CREATE TABLE ratings (id INTEGER PRIMARY KEY,
                                              title VARCHAR(20),
                                              year INT,
                                              rating FLOAT)''')

    def add_rating(self, new_rating):
        try:
            self.execute_sql(
                f'''INSERT INTO ratings (id, title, year, rating) VALUES({new_rating.rating_id},
                '{new_rating.title}', {new_rating.year}, {new_rating.rating});''')
            self.ratings.append(new_rating)
        except sqlite3.DatabaseError as e:
            print(f'Something went wrong when adding new rating: {new_rating}')
            print(e)

